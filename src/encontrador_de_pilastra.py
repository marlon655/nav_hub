def convert_scan_to_real_list(text):
    """Converte o texto do scan para uma lista real de floats em Python."""
    lines = [line for line in text.strip().split('\n') if line.strip()]
    number_list = []
    for line in lines:
        value_str = line.strip()[2:].strip()
        if value_str == '.nan':
            number_list.append(math.nan)
        else:
            number_list.append(float(value_str))
    return number_list




